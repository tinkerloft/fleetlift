	fmt.Println("║   Fleetlift — Local Setup Wizard     ║")
	fmt.Println("╚══════════════════════════════════════╝")
	fmt.Println()

	// Step 1: Preflight checks
	if err := runPreflight(); err != nil {
		return err
	}

